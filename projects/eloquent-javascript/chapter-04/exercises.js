// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end) {
  var array = [];
  if(start === end) {
    return array;
  }else if (start >= 0){
    for (var i = start; i <= end; i++) {
      return i;
    }
  }
  return array;
}

